# -*- coding: utf-8 -*-
from utils.llm_client import build_chain

def extract_meeting_notes(text: str) -> str:
    """
    调用本地大模型提取会议纪要的核心要素
    """
    template = """
你是一个专业的项目管理专家和会议书记员。请根据以下的会议文字记录，提取并整理核心信息。

【会议记录】
{text}

【强制约束与排版要求】
1. 语言必须高度精炼，禁止出现“好的”、“为您整理如下”、“以上就是”等过渡语或解释性废话。
2. 请严格按以下结构输出，使用 Markdown 格式，包含加粗前缀：

**会议主题**：（用一句话总结会议核心目的）
**关键决策**：
（用列表形式列出达成的共识或决定）
**待办事项**：
（用复选框格式列出，必须明确：任务描述 | 责任人 | 截止日期。如果原记录中未提及责任人或日期，请写“待定”）
**未决事项**：
（需要后续讨论或跟进的问题，如无则直接写“无”）

【输出结果】
"""
    chain = build_chain(template)
    return chain.invoke({"text": text})
